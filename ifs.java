import java.util.Scanner;

 class ifs {

	public static void main(String[] args) {

		Scanner in = new Scanner(System.in);
		int a = in.nextInt();
       if(a==1)
       {
           System.out.println("hello");
       }
       else if (a==2){
            System.out.println("namaste");
       }
       else if(a==3){
           System.out.println("bonjour");
       }
     else{
         System.out.println("invalid");
     }

    }
}

